import Foundation

// MARK: - Nft
/// Модель NFT, получаемая из API
struct Nft: Decodable {
    /// Уникальный идентификатор NFT для однозначной идентификации в системе
    let id: String

    /// Название коллекции или произведения NFT
    let name: String

    /// Массив URL-адресов изображений, связанных с данным NFT
    /// Первое изображение обычно используется как обложка
    let images: [URL]

    /// Рейтинг NFT по шкале от 0 до 5 (целое число)
    /// Используется для сортировки и отображения популярности
    let rating: Int

    /// Подробное текстовое описание содержания и особенностей NFT
    let description: String

    /// Цена NFT в ETH (Ethereum), может содержать дробную часть
    /// Используется для покупок и отображения стоимости
    let price: Double

    /// Идентификатор автора/создателя NFT
    /// Может быть использован для получения дополнительной информации об авторе
    let author: String

    /// Дата и время создания NFT в формате ISO 8601
    /// Пример: "2023-12-01T10:30:00.000Z"
    let createdAt: String
}
